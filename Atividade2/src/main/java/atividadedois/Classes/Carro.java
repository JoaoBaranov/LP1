package atividadedois.Classes;

public class Carro {

    private String nome;
    private int ano;
    private String marca;
    private int id;



    public Carro() {
        this.nome = nome;
        this.ano = ano;
        this.marca  = marca;
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

}

