package atividadedois.Classes;

public class Cerveja {

    private String origem;
    private int teor;
    private String marca;
    private int id;



    public Cerveja() {
        this.origem = origem;
        this.teor = teor;
        this.marca  = marca;
        this.id = id;

    }

    public String getOrigem() {
        return origem;
    }

    public void setOrigem(String origem) {
        this.origem = origem;
    }

    public int getTeor() {
        return teor;
    }

    public void setTeor(int teor) {
        this.teor = teor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

}

